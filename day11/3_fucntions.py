# Function are the modules which takes input as parameters and returns output
# We can make user defined functions in python
# We use "def" keywords to create a funtion in python


# Functions can be break down into 3 different parts
# 1. Function definition
# 2. Function call


def addition(a, b):     # Function Definition
    """

    here a and b are the input numbers for the function. The function returns the sum of provided numbers
    'a' and 'b' are the parameters of this function
    """
    return a + b

result = addition(2, 3)     # Function call. Here 2 and 3 are the arguements of the addition function
print(result)   # 5
